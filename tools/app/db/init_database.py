settings = get_settings()

# –°–ø–∏—Å–æ–∫ –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö —Ç–∞–±–ª–∏—Ü
REQUIRED_TABLES = settings.TABLE_NAMES


def check_tables_have_data() -> bool:
    """–ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –µ—Å—Ç—å –ª–∏ –¥–∞–Ω–Ω—ã–µ –≤ —Ç–∞–±–ª–∏—Ü–∞—Ö"""
    try:
        with get_session() as session:
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º –æ—Å–Ω–æ–≤–Ω—É—é —Ç–∞–±–ª–∏—Ü—É tools
            tools_count = session.query(Tool).count()
            logger.info(f"–ó–∞–ø–∏—Å–µ–π –≤ —Ç–∞–±–ª–∏—Ü–µ tools: {tools_count}")

            if tools_count == 0:
                logger.warning("‚ùå –¢–∞–±–ª–∏—Ü–∞ tools –ø—É—Å—Ç–∞")
                return False

            # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ç–∞–±–ª–∏—Ü—ã –≥–µ–æ–º–µ—Ç—Ä–∏–∏
            geometry_tables = [
                (GeometryMillingCutters, "geometry_milling_cutters"),
                (GeometryDrillingCutter, "geometry_drilling_cutter"),
                (GeometryCountersinkingCutter, "geometry_countersinking_cutter"),
                (GeometryDeploymentCutter, "geometry_deployment_cutter"),
                (GeometryTurningCutters, "geometry_turning_cutters"),
            ]

            for model, table_name in geometry_tables:
                count = session.query(model).count()
                logger.info(f"–ó–∞–ø–∏—Å–µ–π –≤ —Ç–∞–±–ª–∏—Ü–µ {table_name}: {count}")

                if count == 0:
                    logger.warning(f"‚ùå –¢–∞–±–ª–∏—Ü–∞ {table_name} –ø—É—Å—Ç–∞")
                    return False

            logger.info("‚úÖ –í—Å–µ —Ç–∞–±–ª–∏—Ü—ã —Å–æ–¥–µ—Ä–∂–∞—Ç –¥–∞–Ω–Ω—ã–µ")
            return True

    except Exception as e:
        logger.error(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –ø—Ä–æ–≤–µ—Ä–∫–µ –¥–∞–Ω–Ω—ã—Ö –≤ —Ç–∞–±–ª–∏—Ü–∞—Ö: {e}")
        return False


